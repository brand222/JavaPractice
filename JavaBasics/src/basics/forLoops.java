package basics;

public class forLoops {
/*This is a simple example of a for loop. It iterates 1 - 3
 * "Starting at 1, iterate while i is less than or equal to 3, and add 1 to i for each iteration
 */
	public static void main(String[] args) {
		// (starting point; conditional stopping point; iterator (counter))
		for (int i = 1; i <= 3; i++) {
			//print i out for each iteration
			System.out.println(i);
		}
	}
}
