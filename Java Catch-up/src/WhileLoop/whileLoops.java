package WhileLoop;

public class whileLoops {

	public static void main(String[] args) {
		int i = 1;
		while (i <= 20) {
			if (i % 2 == 0) {
				System.out.println("Even number!");
			}
			else
			{
				System.out.println("Odd number!");
			}
			System.out.println(i);
		
		}	i++;
	}
}
